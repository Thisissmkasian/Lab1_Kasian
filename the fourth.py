def is_palindrome(s):
    # Видаляємо пробіли і переводимо усі літери в нижній регістр
    s = s.replace(" ", "").lower()
    
    # Порівнюємо рядок з його зворотнім виглядом
    return s == s[::-1]

# Запитуємо користувача ввести рядок
input_string = input("Введіть рядок: ")

# Визначаємо, чи є рядок паліндромом і виводимо результат
if is_palindrome(input_string):
    print("Це паліндром!")
else:
    print("Це не паліндром.")